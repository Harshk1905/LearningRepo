package com.classwork.java;

public class LearningIf {

	public static void main(String[] args) {

		int a = 0;
		if (a > 10) {
			System.out.println("a value is greater then 10");
		} else {
			System.out.println("a value is less then 10");
		}

		// Lader if else

		int b = 20;
		if (b > 20) {
			System.out.println("b value is greater then 20");
		} else if (b < 20) {
			System.out.println("b value is less then 20");
		}

		else {
			System.out.println("Value not found");
		}

	}

}
