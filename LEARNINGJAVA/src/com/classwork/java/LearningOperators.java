package com.classwork.java;

public class LearningOperators {

	public static void main(String[] args) {
		System.out.println(10+5);
		System.out.println(10-5);
		System.out.println(10*5);
		System.out.println(10/5);
		
		//Comperision operator
		
		System.out.println(10>5);
		System.out.println(10<5);
		System.out.println(10==5);
		System.out.println(10<=5);
		System.out.println(10!=5);
		
		//Logical Operators (&&, ||)
		
		System.out.println(10==10 && 10>5);
		System.out.println(10>5 || 10<5);
		
		
	}

}
